public class Ispis{
	public static void main(String[]args){
		double x = 12.4578112;
		String s = "Nikola Nikolic";
		System.out.print("Priprema...");//ispis argumenta
		System.out.print("pozor...");//ispis argumenta
		System.out.println("sad...");//ispis argumenta i prelazak u novi red
		System.out.println("Pobjednik trke je...");//ispis argumenta i prelazak u novi red
		System.out.println();//ispis praznog reda
		System.out.printf("%s sa vremenom %.3f ",s,x);//formatiran ispis
		System.out.println();//ispis praznog reda			
	}
}	